import colors from 'colors';
import mongoose from 'mongoose';
import { Server } from 'socket.io';
import app from './app';
import config from './config';
import process from 'process';
import { socketHelper } from './helpers/socketHelper';
import { errorLogger, logger } from './shared/logger';
import { connectRedis } from './config/radisConfig';

//uncaught exception
process.on('uncaughtException', error => {
  errorLogger.error('UnhandleException Detected', error);
  process.exit(1);
});

let server: any;
async function main() {
try {
  await mongoose.connect(config.database_url as string);
  logger.info(colors.green('ðŸš€ Database connected successfully'));

  //Seed Super Admin after database connection is successful
  // await seedSuperAdmin();
  //  config.ip_address as string,

  const port =
    typeof config.port === 'number' ? config.port : Number(config.port);

  server = app.listen(port, () => {
    logger.info(
      colors.yellow(`â™»ï¸  Application listening on port:${config.port}`)
    );
  });

  //socket
  const io = new Server(server, {
    pingTimeout: 60000,
    cors: {
      origin: '*',
    },
  });
  socketHelper.socket(io);
  //@ts-ignore
  global.io = io;
} catch (error) {
  errorLogger.error(colors.red('ðŸ¤¢ Failed to connect Database'), error);
}


  //handle unhandleRejection
  process.on('unhandledRejection', error => {
    if (server) {
      server.close(() => {
        errorLogger.error('UnhandleRejection Detected', error);
        process.exit(1);
      });
    } else {
      process.exit(1);
    }
  });
}

(async () => {
    await connectRedis()
    await main()
}
)()

//SIGTERM
process.on('SIGTERM', () => {
  logger.info('SIGTERM IS RECEIVE');
  if (server) {
    server.close();
  }
});
